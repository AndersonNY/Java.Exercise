public class LoopsClass4 {

    //Task is to print even numbers from 1 to 10
    //use for statement and use i = i + 1
    //OUTPUT: 2,4,6,8,10

    public static void main(String[] args) {


        for (int i = 0; i <= 10; i = i + 2) {
            System.out.println(i);


        }


    }
}

