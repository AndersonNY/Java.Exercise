package src.Day10;

public class LoopsClass1 {

    public static void main(String[] args) {
        for (int i = 1; i <= 31; i += 10) {
            System.out.println(i);
        }


    }

    private static void forLoopExample() {

//        i++ is the same thing as i = i + 1;
        for (int i = 1; i <= 31; i += 10) {
            System.out.println(i);
        }
    }


}





