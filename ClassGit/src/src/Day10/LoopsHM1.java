package src.Day10;

public class LoopsHM1 {

    public static void main(String[] args) {

        for (int i= 0; i<10;i++) {

            // loop variable

            System.out.println("Message" + i);
            i++;

        }

        int i = 0;
        while (i<10) {

            System.out.println( "message" + i );

            i++;


        }

        i=0;

        do {
            System.out.println("second message" + i);

            i++;
        } while (i <10);






    }
}
