package src.Day8;

public class IfElse2 {


    public static void main(String[] args) {
        int number = 3;

        if (number > 5) {
            System.out.println("Number is bigger than 5");
        } else if (number > 3) {
            System.out.println("Number is bigger than 3");
        } else {
            System.out.println("Number is less than 3");
        }


    }



}


